    # ‚≠êÔ∏è 3. ‡∏õ‡∏£‡∏±‡∏ö handle ‡πÉ‡∏´‡πâ‡∏£‡∏±‡∏ö Dictionary ‡πÅ‡∏•‡∏∞‡∏°‡∏µ Error Handling ‡∏ó‡∏µ‡πà‡∏î‡∏µ‡∏Ç‡∏∂‡πâ‡∏ô ‚≠êÔ∏è
    def handle(self, synthesis_order: Dict[str, Any]) -> str:
        """
        ‡∏£‡∏±‡∏ö "‡πÅ‡∏ü‡πâ‡∏°‡∏á‡∏≤‡∏ô‡∏ö‡∏£‡∏£‡∏ì‡∏≤‡∏ò‡∏¥‡∏Å‡∏≤‡∏£" (synthesis_order) ‡∏à‡∏≤‡∏Å Dispatcher ‡∏°‡∏≤‡∏õ‡∏£‡∏∞‡∏°‡∏ß‡∏•‡∏ú‡∏•
        """
        raw_draft = synthesis_order.get("draft_to_review", "")
        if not raw_draft or not isinstance(raw_draft, str):
            return "" # ‡∏Ñ‡∏∑‡∏ô‡∏Ñ‡πà‡∏≤‡∏ß‡πà‡∏≤‡∏á‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏£‡πà‡∏≤‡∏á‡∏Ñ‡∏≥‡∏ï‡∏≠‡∏ö

        original_query = synthesis_order.get("original_query", "(‡πÑ‡∏°‡πà‡∏£‡∏∞‡∏ö‡∏∏)")

        print("‚úçÔ∏è [Formatter Agent] Requesting a key for final typesetting...")
        api_key = self.key_manager.get_key()
        if not api_key:
            print("‚ö†Ô∏è FormatterAgent: No API keys available. Returning raw draft.")
            return raw_draft

        try:
            genai.configure(api_key=api_key)
            model = genai.GenerativeModel(self.model_name)
            
            prompt = self.formatting_prompt_template.format(
                original_query=original_query,
                draft_to_review=raw_draft
            )
            response = model.generate_content(prompt)
            return response.text.strip()
            
        except Exception as e:
            error_str = str(e).lower()
            if "429" in error_str: # ‡∏î‡∏±‡∏Å‡∏à‡∏±‡∏ö Rate Limit Error ‡πÑ‡∏î‡πâ‡∏Å‡∏ß‡πâ‡∏≤‡∏á‡∏Ç‡∏∂‡πâ‡∏ô
                print(f"üü° Formatter Agent: Key '...{api_key[-4:]}' hit rate limit.")
                self.key_manager.report_failure(api_key)
                print("   -> Retrying with the next available key...")
                return self.handle(synthesis_order) # Retry ‡πÇ‡∏î‡∏¢‡∏™‡πà‡∏á synthesis_order ‡πÄ‡∏î‡∏¥‡∏°‡πÑ‡∏õ
            else:
                print(f"‚ùå An unexpected error occurred in Formatter Agent: {e}")
                return raw_draft # Fallback to raw text